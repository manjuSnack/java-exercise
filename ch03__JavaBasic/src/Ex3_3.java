public class Ex3_3 {

	public static void main(String[] args) {
		int i=5, j=5;
		System.out.println(i++); // i의 값을 출력 후, 1증가
		System.out.println(++i); // j의 값을 1 증가 후, 출력
		System.out.println("i = " + i + ", j = " + j);
	}

}
